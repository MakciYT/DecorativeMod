package makciyt.decorativemod.container;

import makciyt.decorativemod.DecorativeMod;
import net.minecraft.inventory.container.ContainerType;
import net.minecraft.util.math.BlockPos;
import net.minecraft.world.World;
import net.minecraftforge.common.extensions.IForgeContainerType;
import net.minecraftforge.eventbus.api.IEventBus;
import net.minecraftforge.fml.RegistryObject;
import net.minecraftforge.registries.DeferredRegister;
import net.minecraftforge.registries.ForgeRegistries;

public class ModContainers {

    public static DeferredRegister<ContainerType<?>> CONTAINERS
            = DeferredRegister.create(ForgeRegistries.CONTAINERS, DecorativeMod.MOD_ID);

    public static final RegistryObject<ContainerType<WardrobeContainer>> WARDROBE_CONTAINER = CONTAINERS.register("wardrobe_container",
            () -> IForgeContainerType.create(((windowId, inv, data) -> {
                BlockPos pos = data.readBlockPos();
                World world = inv.player.getEntityWorld();
                return new WardrobeContainer(windowId, world, pos, inv, inv.player);
            })));
	public static final RegistryObject<ContainerType<FridgeContainer>> FRIDGE_CONTAINER = CONTAINERS.register("fridge_container",
            () -> IForgeContainerType.create(((windowId, inv, data) -> {
                BlockPos pos = data.readBlockPos();
                World world = inv.player.getEntityWorld();
                return new FridgeContainer(windowId, world, pos, inv, inv.player);
            })));


    public static void register(IEventBus eventBus) {
        CONTAINERS.register(eventBus);
    }
}